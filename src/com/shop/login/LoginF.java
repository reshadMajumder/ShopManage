    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String u_name=txtUserName.getText();
        String pass = txtPassword.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/shop_manage","root","Majumder@reshad");
            insert = con1.prepareStatement("select * from user where user_name=? and password=? ");
            
            insert.setString(1, u_name);
            insert.setString(2, pass);
            rs=insert.executeQuery();
            
            if (rs.next()) {
                //JOptionPane.showMessageDialog(rootPane, "you are logged in");
                new Home().setVisible(true);
                this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Login Failed!\nTry again or Register");

            }
            
        } catch (Exception e) {
            System.out.println("database not connected");
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

